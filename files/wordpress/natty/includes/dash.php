		<h6>Use search to quickly find applications you know the name of</h6>
		<p>If you're looking for a particular application (say, Firefox) and you know the name of it, then the easiest way to find it in the Dash is simply by searching.</p>
		<p>Begin by typing the first few letters and the Unity Dash will filter results for you as you type. </p>
		<p>When you see what you want, click it or press Enter to launch.</p>

		<h6>Filter applications by category</h6>
		<p>Don't know the name of the application you're looking for? No worries. Narrow down the results by choosing a category such as Internet, Multimedia, or even Preferences and you might have more luck finding what you want.</p>

		<h6>Use the Applications and Places lenses</h6>
		<p>Unity has separate "Lenses" that give you easy access to your Applications, and Files and Folders. You can access them by clicking the icons near the bottom of the Launcher.</p>
		<p>These Lenses work much the same way as the main Dash, except that they are specialized for a particular job.</p>

		<h6>Drag and drop applications from the Dash</h6>
		<p>You can drag applications onto the launcher from the dash.</p>
		<p>1. Open up the Unity dash by clicking the Ubuntu logo in the left corner.</p>
		<p>2. Find the application you're looking for.</p>
		<p>3. Drag the application icon from the Dash to a position on the Launcher.</p>
		
		<h6>Multi-task easily with window snapping</h6>
		<p>A new feature in Ubuntu 11.04 is window snapping. Simply drag a window to either the top, left, or right edges of the screen to activate the snapping feature.</p>
		<p>Using this method, you can easily have two applications side by side for easy multi-tasking.</p>
