	<div class="left">
		<h6>Redesigned indicator area</h6>
		<p>The panel area on the right side of the screen has a new behaviour. Say goodbye to confusing and inconsistent right click menus, welcome to the age of indicators.</p>
		<p>This area serves to notify you of things like instant messages and new emails, as well as provide quick access to commonly used actions like logging out or switching to the next track in your music player.</p>

		
		<h6>Global menu for applications</h6>
		<p>Just like Mac OS X, Ubuntu now has global menus for applications in the panel, rather than in the application window itself.</p>
		<p>The menu for applications is traditionally filled with entries such as File, Edit, View etc.</p>
		<p>This minimizes clutter in each application window, allowing you to focus easily on the task at hand. The menus aren’t visible when you don’t need them, so they hide away until you roll over the area where they are. </p>
		<p>To show them, simply move your mouse over the name of the application in the panel, to the right of the Ubuntu button in the corner.</p>
		<p>The application main menu will appear and you can click on the entries to find advanced options and actions.</p>
		
		<h6>New sound menu, session menu, and clock</h6>
		<p>Want to switch to the next track in your music player? How about easily checking what time it is in Los Angeles?</p>
		<p>With redesigned indicator menus, you can do all of this and more easily, and it’s only a click away in the top right corner all the time.</p>

		<h6>Window controls in the panel for maximized applications</h6>
		<p>When you maximize an application, the window controls (Close, Minimize, Maximize) are moved into the panel to give you more vertical working space.</p>
