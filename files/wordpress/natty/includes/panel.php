		<h6>Access to system preferences and settings is now in the session menu</h6>
		<p>Looking for system settings and preferences? These can now be found in the session menu - just click on the shutdown button in the top right corner and choose <b>System Settings</b> at the bottom.</p>
		<p>This will launch the new Control Center in Ubuntu 11.04 which contains all the previous entries that used to be in the "System" menu now in one single window.</p>
